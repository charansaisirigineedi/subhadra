<?php

include 'connect.php';

$dob = $_GET['dob'];

function ageCalculator($dob){
    if(!empty($dob)){
        $birthdate = new DateTime($dob);
        $today   = new DateTime('today');
        $age = $birthdate->diff($today)->y;
        return $age;
    }else{
        return 0;
    }
}
echo ageCalculator($dob);
?>